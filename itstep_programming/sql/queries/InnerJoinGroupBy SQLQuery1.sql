SELECT a.[CUSTOMER_ID], a.[NAME], SUM(b.[AMOUNT]) AS [TOTAL AMOUNT]
FROM RES_DATA a INNER JOIN INV_DATA b
ON a.[CUSTOMER_ID]=b.[CUSTOMER_ID]
GROUP BY a.[CUSTOMER_ID], a.[NAME]
